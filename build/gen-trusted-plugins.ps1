<#
.SYNOPSIS
    Pins the first-party strategy plugins this build produced: writes plugins-trusted.json into the
    staged plugins root, listing every assembly of every plugin folder with its sha256.

.DESCRIPTION
    This is how the shipped app trusts its OWN plugins without a code-signing certificate. The Curated
    trust policy accepts a plugin whose folder hashes exactly to what the build staged (see
    PluginTrustedHashes), so the shipped strategy catalogue loads silently under Curated, while
    third-party plugins still have to be signed or explicitly consented to.

    It doubles as the integrity baseline: if any shipped assembly is later modified, swapped, added or
    removed, the plugin no longer matches and the loader quarantines it as tampered instead of running
    rewritten code.

    Called from each edition shell's StrategyPlugins.targets after the Stage* targets. ONE shared copy:
    this is a build script, not shell code, so it does not follow the x3 shell-copy rule.

    NOTE: keep this file ASCII-only. Windows PowerShell 5.1 reads a BOM-less .ps1 as ANSI, so non-ASCII
    characters (em dashes, arrows) corrupt string literals and the script fails to parse.

.PARAMETER PluginsRoot
    The staged plugins folder: {OutDir}/plugins.
#>
[CmdletBinding()]
param(
    [Parameter(Mandatory = $true)]
    [string] $PluginsRoot
)

$ErrorActionPreference = 'Stop'

if (-not (Test-Path -LiteralPath $PluginsRoot)) {
    Write-Host "gen-trusted-plugins: no plugins staged at '$PluginsRoot', nothing to pin."
    exit 0
}

$plugins = @()

foreach ($dir in Get-ChildItem -LiteralPath $PluginsRoot -Directory | Sort-Object Name) {
    $assemblies = [ordered]@{}

    foreach ($dll in Get-ChildItem -LiteralPath $dir.FullName -Filter *.dll -File | Sort-Object Name) {
        $assemblies[$dll.Name] = (Get-FileHash -LiteralPath $dll.FullName -Algorithm SHA256).Hash
    }

    # A folder with no assemblies is not a plugin; pinning it would pin emptiness.
    if ($assemblies.Count -eq 0) { continue }

    $plugins += [pscustomobject]@{
        plugin     = $dir.Name
        assemblies = $assemblies
    }
}

$document = [pscustomobject]@{
    _comment  = 'Generated by build/gen-trusted-plugins.ps1. The sha256 of every assembly in every first-party plugin folder, as shipped. The Curated trust policy accepts a plugin whose folder matches exactly; any difference is treated as tampering. Do not hand-edit.'
    generated = (Get-Date).ToUniversalTime().ToString('u')
    plugins   = $plugins
}

$path = Join-Path $PluginsRoot 'plugins-trusted.json'
$document | ConvertTo-Json -Depth 5 | Set-Content -LiteralPath $path -Encoding utf8

Write-Host "gen-trusted-plugins: pinned $($plugins.Count) plugin(s) into $path"
